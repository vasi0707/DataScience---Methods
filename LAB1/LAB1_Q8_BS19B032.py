#chr() function
#returns characters as per ASCII code for given number

print("The chr function for input = 67:")
print(chr(67))
print("")
print("The chr function for input = 97:")
print(chr(97))
print("")

#ord() function
#returns ASCII code for the given character

print("The ord function for input = /'C/':")
print(ord('C'))
print("")
print("The ord function for input = /'a/':")
print(ord('a'))
